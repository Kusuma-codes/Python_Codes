# Python Program to Slice Lists

numbers = [10, 20, 30, 40, 50, 60, 70, 80, 90]

print("Original List:", numbers)

print("Sliced list [2:6]:", numbers[2:6])

print("Sliced list [:5]:", numbers[:5])

print("Sliced list [3:]:", numbers[3:])

print("Sliced list [::2]:", numbers[::2])

print("Reversed List:", numbers[::-1])
