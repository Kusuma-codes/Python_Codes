#Math Opeartion
import math

num = float(input("Enter a number: "))

print("\nMath Operations using math library:")
print("Square root:", math.sqrt(num))
print("Power (num^2):", math.pow(num, 2))
print("Ceiling value:", math.ceil(num))
print("Floor value:", math.floor(num))
print("Factorial (only works for integers):", math.factorial(int(num)))
print("Sine of number (in radians):", math.sin(num))
print("Cosine of number (in radians):", math.cos(num))
print("Logarithm base e:", math.log(num))
